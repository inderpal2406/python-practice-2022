# This script will display location of site-packages folder on the system.
# site-packages is the target directory of manually built python packages.
# When we build and install python packages from source (using disutils, probably by executing python setup.py install),
# we'll find the installed modules in site-packages by default. (This is as per stackoverflow).
# I have observed that there are multiple site-packages directories on the system.
# In one of these site-packages folder, there are files (python code) present for the modules which we install.
# Such modules are installed as these are not present by default in python. 
# For example, awscli, boto3, xlwt, etc.
# Such modules are installed using pip command.

# Import modules.

import clear_screen_module
import site

# Define functions.

def main():
    # Clear the screen.
    clear_screen_module.clear_screen()
    # Display purpose of the script.
    print(f"This script will display the location of site-packages folders on the system.\n")
    # Store site-packages folders' locations in list.
    site_pkg_locations_list = site.getsitepackages()
    # Print the locations one by one.
    print(f"The site-packages are available at below locations:\n")
    for site_pkg_location in site_pkg_locations_list:
        print(f"{site_pkg_location}")
    print()     # Leave a line at the end.
    return None

# Main code starts here.

if __name__ == "__main__":
    main()
